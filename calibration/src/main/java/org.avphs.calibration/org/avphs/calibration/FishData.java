package org.avphs.calibration;

public class FishData {
    private final float degree;
    private final float error;

    public FishData(float degree, float error) {
        this.degree = degree;
        this.error = error;
    }

    public float getDegree() {
        return error;
    }

    public float getError() {
        return error;
    }
}
